"""Tuples: swap two non-negative integers."""

from __future__ import annotations


def swap_pair(a: int, b: int) -> tuple[int, int]:
    """Return exactly the pair (b, a). Inputs are non-negative.

    A functional-correctness theorem should characterize the returned pair
    directly, not only show that swapping twice is an involution.
    """
    return (b, a)
